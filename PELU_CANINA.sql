USE PELUQ_CANINA;
CREATE TABLE DUENO(
DNI INT(8) PRIMARY KEY NOT NULL,
NOMBRE VARCHAR (20),
APELLIDO VARCHAR (20),
TELEFONO INT(12),
DIRECCION VARCHAR (30)
);

CREATE TABLE PERRO(
ID_PERRO INT (2) UNSIGNED NOT NULL PRIMARY KEY,
NOMBRE VARCHAR (15),
FECHA_NAC DATE NOT NULL,
SEXO CHAR (12), 
DNI_DUEÑO INT (8) UNSIGNED NOT NULL UNIQUE
);

CREATE TABLE HISTORIAL(
ID_HISTORIAL INT UNSIGNED NOT NULL,
FECHA DATE NOT NULL,
PERRO VARCHAR (60),
MNTO INT (8)
);

